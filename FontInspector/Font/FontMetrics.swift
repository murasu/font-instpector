import Foundation

struct FontMetrics {
    var baseline:Float = 0
    // Scaled font-ascent metric
    var ascent:Float = 0
    // Scaled font-descent metric
    var descent:Float = 0
    // Leading
    var leading:Float = 0
    // Cap Height
    var capHeight:Float = 0
    // x height
    var xHeight:Float = 0
    // Underline Position
    var underline:Float = 0
    // Underline Thickness
    var ulThickness:Float = 0
    // UPM
    var upem:Float = 1024
}

